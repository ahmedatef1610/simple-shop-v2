const mongoose = require("mongoose");
/***********************************************************************/
const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      require: true,
    },
    email: {
      type: String,
      require: true,
    },
    password: {
      type: String,
      require: true,
    },
    isAdmin: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
  }
);
/***********************************************************************/
module.exports = mongoose.model("user", userSchema); // name change to lowercase and add s in end
